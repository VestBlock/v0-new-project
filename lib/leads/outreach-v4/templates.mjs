function fillTemplate(text, lead) {
  return text
    .replaceAll('{{businessName}}', lead.businessName || lead.propertyAddress || 'your team')
    .replaceAll('{{city}}', lead.city || 'your market')
}

function selectFirstTouchVariant(library, lead) {
  if (lead.vertical !== 'real_estate_partners') return library.firstTouch[0]

  const context = [
    lead.businessName,
    lead.niche,
    lead.serviceFit,
    lead.painSignal,
    ...(Array.isArray(lead.evidence) ? lead.evidence : []),
  ]
    .filter(Boolean)
    .join(' ')
    .toLowerCase()

  if (/lender|lending|loan|mortgage|capital|finance|hard money|private money/.test(context)) {
    return library.firstTouch[1]
  }
  if (/investor|investment|wholesale|buyer|acquisitions|cash buyer/.test(context)) {
    return library.firstTouch[0]
  }
  if (/agent|broker|realtor|seller|listing|property management|manager/.test(context)) {
    return library.firstTouch[2]
  }
  return library.firstTouch[0]
}

export function draftOutreachV4(lead) {
  const library = V4_TEMPLATE_LIBRARY[lead.vertical] || V4_TEMPLATE_LIBRARY.search_visibility
  const variant = selectFirstTouchVariant(library, lead)
  return {
    leadId: lead.id,
    vertical: lead.vertical,
    channel: 'email',
    subject: fillTemplate(variant.subject, lead),
    body: `${fillTemplate(variant.body, lead)}\n\nBest,\nRob\nVestBlock\n\nIf this is not relevant, reply no and I will not follow up.`,
    status: lead.manualReviewOnly || lead.vertical === 'distressed_house' ? 'manual_review_required' : 'draft_ready',
    complianceNote: 'No guarantees of rankings, AI citations, funding, legal outcomes, property sale outcomes, or payout outcomes.',
  }
}
